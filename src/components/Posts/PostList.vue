<template>
    <div class="mt-4">
      <div
        v-for="post in filteredPosts"
        :key="post.node.id"
      >
        <PostPanel :post="post.node" v-if="post.node.publish == 'yes'"/>
      </div>
    </div>
</template>

<static-query>
query Posts {
  posts: allPost(sortBy: "date", order: DESC) {
    edges {
      node {
        id
        title
        author
        tags {
          id
          title
        }
        path
        date
        publish
        excerpt
      }
    }
  }
}
</static-query>

<script>
import PostPanel from './PostPanel'

export default {
  components: {
    PostPanel
  },

  props: {
    filter: {
      type: String,
      required: true
    }
  },
  
  computed: {
    filteredPosts () {
      return this.$static.posts.edges.filter(edge => {
        return this.filter === 'All' ? edge : edge.node.tags.some(e => e.title === this.filter)
      })
    }
  }
}
</script>

